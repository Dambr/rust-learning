/**
 * Multiple line comment
 */
fn main() {
    // Single lime comment

    let num: u128 = 0;
    println!("Result: {}", num);

    let num: i128 = 1;
    println!("Result: {}", num);

    let num: f32 = 2.5;
    println!("Result: {}", num);
    
    let num: bool = false;
    println!("Result: {}", num);

    let num: char = 'h';
    println!("Result: {}", num);
}
